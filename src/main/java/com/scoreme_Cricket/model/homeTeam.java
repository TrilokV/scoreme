package com.scoreme_Cricket.model;

public class homeTeam {

	private String homeTeamName;
	int homeruns, homewickets, homeextras;
	double homeovers, homerunrate, homeoverslimit;
	public String getHomeTeamName() {
		return homeTeamName;
	}
	public int getHomeruns() {
		return homeruns;
	}
	public void setHomeruns(int homeruns) {
		this.homeruns = homeruns;
	}
	public int getHomewickets() {
		return homewickets;
	}
	public void setHomewickets(int homewickets) {
		this.homewickets = homewickets;
	}
	public int getHomeextras() {
		return homeextras;
	}
	public void setHomeextras(int homeextras) {
		this.homeextras = homeextras;
	}
	public void setHomeTeamName(String homeTeamName) {
		this.homeTeamName = homeTeamName;
	}
	public double getHomeovers() {
		return homeovers;
	}
	public void setHomeovers(double homeovers) {
		this.homeovers = homeovers;
	}
	public double getHomerunrate() {
		return homerunrate;
	}
	public void setHomerunrate(double homerunrate) {
		this.homerunrate = homerunrate;
	}
	public double getHomeoverslimit() {
		return homeoverslimit;
	}
	public void setHomeoverslimit(double homeoverslimit) {
		this.homeoverslimit = homeoverslimit;
	}
	

}
